// Static function
// Example, write a function that capitalizes a string

let myString = "endri";

function capitalizeString () {
    console.log(myString.toUpperCase());
}
capitalizeString();

// Pure function
// Example, write a function that capitalizes a string

function pureCapitalizeString(str) {
    return str.toUpperCase();
}
console.log(pureCapitalizeString("pure endri"));


